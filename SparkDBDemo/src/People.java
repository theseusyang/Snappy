
public class People {
	
	public Integer Id;
	
	public String Name;
	public Integer TaxNumber;
	
	public Integer getId() {
		return Id;
	}
	public void setId(Integer id) {
		Id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public Integer getTaxNumber() {
		return TaxNumber;
	}
	public void setTaxNumber(Integer taxNumber) {
		TaxNumber = taxNumber;
	}
}
